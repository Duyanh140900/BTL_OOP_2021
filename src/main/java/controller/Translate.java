    public Translate() throws IOException {
        initComponents();
        this.setLocationRelativeTo(null);
        txtTranslate.setEditable(false);

        //listYourDicts = new ArrayList<>();
        dic = new Dictionary();
        // đọc file
        dic.loadFromFile();
        //sắp xếp các từ trong file
        dic.sortWords();
        // lấy danh sách các từ có trong file
        words = dic.getWords();

        Iterator<Word> itr = words.iterator();
        //thêm các từ tiếng anh, tiếng việt vào danh sách và thêm các từ vào từ điển Hashtable
        while (itr.hasNext()) {
            Word word = itr.next();
            En.add(word.getWord());
            Vn.add(word.getPhonetics() + "\n" + word.getMeaning());
            dictionary.put(word.getWord(), word.getPhonetics() + "\n" + word.getMeaning());
        }
    }

