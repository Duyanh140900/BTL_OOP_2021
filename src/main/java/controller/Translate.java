    // tìm kiếm từ theo từ khóa
    private void txtSearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyTyped
        // lấy giá trị từ ô search
        String str = txtSearch.getText().trim();
        str += evt.getKeyChar();
        str = str.toLowerCase().trim();
        String s;
        // duyệt qua các từ có trong từ điển 
        for (int i = 0; i < mod.size(); i++) {
            s = mod.getElementAt(i);
            s = s.toLowerCase();
            // kiểm tra tiền tố của chuỗi có khớp với tiền tố nhập vào hay không
            if (s.startsWith(str)) {
                // set Selected
                listDictionary.setSelectedIndex(i);

                //txtSearch.setText(s);
                JScrollBar sb = jScrollPane3.getVerticalScrollBar();
                sb.setValue(i * 20);
                checkNotFound = true;
                return;
            }
        }
        // nếu không có thì thông báo not found
        txtTranslate.setText("Không có từ này.\nBạn có thể sử dụng từ điển cá nhân hoặc API Google để tra cứu");
        checkNotFound = false;
        listDictionary.clearSelection();
    }//GEN-LAST:event_txtSearchKeyTyped

